          <h2 class="text-light text-center">¿Como se llega al Mitclan?</h2>
        </div>
        <div class="col-sm-12 md-6">
          <p class="text-light text-center">Para poder llegar al Mitclán devemos pasar por nueve niveles o pisos por haci decirlo</p>
          <p class="text-light text-center">Estos son los nueve niveles Diferentes antes de llegar completamente al Mitclán</p>
          <!--- empieza lista --->
          <ul class="list-group list-group-flush"  style="background-color:#7F2A11">
            <li class="list-group-item text-light" style="background-color:#B8401D">Primer nivel: Chiconahuapan También llamado Itzcuintlan o “lugar de perros”,
                                                                                    este sitio estaba a la orilla de un caudaloso río, que el muerto debía atravesar
                                                                                    con la ayuda de un xoloitzcuintle de color pardusco</li>
            <li class="list-group-item text-light" style="background-color:#7F2A11">Segundo Nivel: Tepectli Monamictlan El “lugar de los cerros que se juntan”.
                                                                                    En este nivel se dice que existían dos cerros que se abrían y se cerraban,
                                                                                    chocando entre sí de manera continua. Los muertos, por lo tanto,
                                                                                    debían buscar el momento oportuno para cruzarlos sin ser triturados.</li>
            <li class="list-group-item text-light" style="background-color:#B8401D">Tercer Nivel: Iztepetl
                                                                                    En este lugar se encontraba un cerro cubierto de filosísimos pedernales,
                                                                                    que desgarraban los cadáveres de los muertos cuando estos tenían que escalarlos para cumplir con su trayectoria.</li>
            <li class="list-group-item text-light" style="background-color:#7F2A11">Cuarto Nivel: Itzehecayan
                                                                                    El “lugar del viento de obsidiana” era un sitio desolado de hielo y piedra abrupta.
                                                                                    Se trata de una sierra con aristas cortantes compuesta de ocho collados en los que siempre caía nieve.</li>
            <li class="list-group-item text-light" style="background-color:#B8401D">Quinto Nivel: Paniecatacoyan
                                                                                    “El lugar donde la gente vuela y se voltea como banderas”.
                                                                                     Se dice que este lugar se ubicaba al pie del último collado o colina del Itzehecayan,
                                                                                     donde los muertos perdían la gravedad y estaban a merced de los vientos,
                                                                                     que los arrastraba hasta que finalmente eran liberados para pasar al nivel siguiente.</li>
            <li class="list-group-item text-light" style="background-color:#7F2A11">Sexto Nivel: Timiminaloayan “El lugar donde la gente es flechada”.
                                                                                    Aquí existía un extenso sendero a cuyos lados manos invisibles enviaban puntiagudas saetas para acribillar a los cadáveres
                                                                                    de los muertos que lo atravesaban. Estas eran saetas perdidas durante las batallas.</li>
            <li class="list-group-item text-light" style="background-color:#B8401D">Séptimo Nivel: Teocoyohuehualoyan Aquí los jaguares abrían el pecho del muerto para comerse su corazón.</li>
            <li class="list-group-item text-light" style="background-color:#7F2A11">Octavo Nivel: Izmictlan Apochcalolca
                                                                                    En esta “laguna de aguas negras” (Apanhuiayo),
                                                                                    el muerto terminaba de descarnar y su tonalli (su alma), se liberaba completamente del cuerpo.</li>
            <li class="list-group-item text-light" style="background-color:#B8401D">Noveno Nivel: Chicunamictlan
                                                                                    Aquí el muerto debía atravesar las nueve aguas de Chiconauhhapan y, una vez superado este último obstáculo,
                                                                                    su alma sería liberada completamente de los padecimientos del cuerpo, por Mictlantecuhtli y Mictecacihuatl,
                                                                                    esencia de la muerte masculina y femenina respectivamente.
                                                                                    Para transitar estas pruebas, el difunto debía ir surtido de algunos amuletos y pertenencias para facilitar su camino, entre agua, mantas y armas.
                                                                                    Al final de este largo viaje al Mictlán, el difunto debía entregar a Mictlantecuhtli los
                                                                                    tributos que se le habían entregado antes de despertar a la orilla del río,
                                                                                    pues no sería justo llegar ante el mismísimo señor de la muerte con las manos vacías.</li>
          </ul>
